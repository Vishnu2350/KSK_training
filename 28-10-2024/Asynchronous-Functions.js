console.log("Script start"); // Step 1

// Fetching Data - simulated using a Promise
const fetchData = () => {
  return new Promise((resolve) => {
    setTimeout(() => {
      resolve("Fetched data");
    }, 3000); // 3-second delay
  });
};

// Simulating another asynchronous task
setTimeout(() => {
  console.log("Timeout completed"); // Step 2
}, 2000); // 2-second delay

// Step 3: Call the asynchronous function
fetchData().then((data) => {
  console.log(data); // Logs 'Fetched data' after 3 seconds
});

console.log("Script end"); // Step 4

/* Operation 1: Function Declaration */

function add(a, b) {
    return a + b;
  }
  
  console.log(add(3, 5));

  /* Operation 2: Arrow Function */

  const multiply = (x, y) => x * y;
console.log(multiply(4, 7));

/* Operation 3: Function Expression */

const subtract = function(a, b) {
    return a - b;
  };
  console.log(subtract(10, 6));
  